
def basic_damege(sts_str):
    return (sts_str / 2) * 0.5

def knife_damge(sts_str):
    return basic_damege(sts_str) + 5

def sword_damge(sts_str):
    return basic_damege(sts_str) + 10

def long_sword_damge(sts_str):
    return basic_damege(sts_str) + 15

def spear_damge(sts_str):
    return basic_damege(sts_str) + 15
